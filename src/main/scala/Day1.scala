object Day1 {

  def sumDuplicates(input: String): Int = {
    val digits: Array[Int] =
      input
        .toCharArray
        .map(_.asDigit)

    val pairs: List[(Int, Int)] = digits.zip(digits.tail).toList

    ((input.head.asDigit, input.last.asDigit) :: pairs)
      .filter { case (a, b) => a == b }
      .foldLeft(0) { case (acc, (a, _)) => acc + a }
  }

  def sumMirrors(input: String): Int = {
    val (first, second) =
      input.map(_.asDigit).splitAt(input.length / 2)

    first.zip(second)
      .filter { case (a, b) => a == b }
      .foldLeft(0) { case (acc, (a, _)) => acc + 2 * a }

  }

